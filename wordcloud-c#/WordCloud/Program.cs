</p><h3>第六章　　日本における自転車の利用拡大に向けての問題点と解決方法</h3>
<p>
政府にも自転車を利用してほしい理由があり、人々にとっても自転車を使うメリットはある。なのになぜ自転車を利用する人が少ないのか？それは自転車を利用する際のデメリットが大きいからだと思います。</p><p>
<font size="5">自転車の利用を阻んでいる問題</font>

</p><p><br>日本が抱える自転車の問題点は
「安全な走行環境が少ない。」「駐輪スペースがなく、放置自転車が多い。」「自転車の窃盗が多発している。」「販売店の衰退により修理やメンテナンスが困難。」「ルールを守らない暴走自転車による事故が多発。」


</p><p><br>「安全な走行環境がない。」
日本において自転車は原則車道を走ることになっている。しかし車道は車一台がぎりぎり通れるところが多く、自転車が走るのに十分なスペースがないのが現状である。そのため歩道を走る自転車が多く歩行者と接触事故を起こすケースが多々ある。

</p><p><br>→対策　
安全な走行環境の確保が現状では難しい。それは、現状ある行動を走る手段の中に「クルマ」「歩行者」「自転車」があるが現在ではまだ自転車の優先度合いや使用割合が低いからである。今後、より自転車の促進を進めることで行動の優先度合いも変化し、安全な走行環境が確保されると思う。


</p><p><br>「ルールを守らない暴走自転車による事故が多発。」

</p><p><br>自転車はクルマと同じ車両である。最悪の場合人の命を奪ってしまう危険性もある。しかしそのことを意識している人が少ないと思う。スマホをいじりながら走ったり、平気で逆走したりする現状があり自転車のマナーが問題視されている。このことが自己を引き起こす可能性がとても高いのでマナー向上は重要である。
対策→効果的な対策は罰則を強化し、取り締まりを厳しくすることである。「ムチ」を厳しくすることでルールの徹底を促すことができる。この法規制・取り締まりの強化は今後加速していくと考えられる。それは、政府が国策として自転車の重要性を認識し始めてきているからだ。



</p><p><br>「駐輪スペースがなく、放置自転車が多い。」
放置自転車は交通渋滞や事故の原因、身体障碍者や救急自動車の通行の妨げ、町の美観を損なうなど様々な問題の原因になる。<br>統計によると1日に２４万台もの自転車が現在放置されている。<br>国がこの放置自転車を撤去するのにも莫大な税金を投入されており放置自転車を解決する必要がある。この問題の原因「駐輪スペースの少なさ」と「料金の高さ」である。　駅前は地価が高く広い駐輪スペースを確保すると、駐輪所の管理・運営のために多くのコストがかかり料金が高くなってしまう。そのことで違法に駐輪する人が増えてしまう。
<br>
</p><p>対策→ただ自転車を停めるだけではコストが高くなってしまう。解決策は「立体駐輪場の建設など、よりコンパクトに収納する。」か「止めてある自転車を有効活用することで収益を得るなどして料金を賄う。」ことが求められる。


<br></p><p>「自転車の窃盗が多発している。」

</p><p>自転車は比較的短時間に簡単に盗難することができるので狙われることが多い。日本では報告されているだけでも年間４０万件も発生している。ほかにもパーツを取り外して持って行ってしまうなどのいたずらも多く発生しているので安心して駐輪できる環境を整備する必要がある。
対策→自転車のカギを壊されにくいものにすることも大切だが、自転車のセキュリティを保つためには駐輪所に止めて管理してもらう必要がある。</p><p>


</p><p><br>「整備不足により危険な自転車が多い。」

</p><p><br>自転車の性能が年々向上していることに比例して、メンテナンスの重要性も増してきています。電動になったり、繊細な素材を使っていたりなど整備がより困難になってきています。<br>また、一昔前は「日曜大工」という言葉がはやり、自分で簡単な修理やメンテナンスを行うことができる人が多かったです。しかし現在の若者は工具を持っている人すら少なく、簡単なパンク修理ですらできない人が多いです。これは今後どんどん修理できない人が増え、専門の技術者の需要が高まります。
<br>対策→専門の技術者を確保することが重要ですが現在、自転車業を営んでいる人の数が急速に減少しています。その理由は、アマゾンなどのインターネットショッピングの充実により自転車販売店がビジネスとして成り立たなくなっていることにあります。<br>自転車は近年、とても多くの種類やデザインが誕生し購入時の選択肢の幅がとても広いです。<br>しかし自転車はスペースを多くとるため、一店舗が保有しておくことができる自転車の種類は限られています。また、管理コストが代金に追加されるので割高になります。ネットで購入すれば様々なジャンル・デザインが選べてかつ代金も安いため今後、既存の自転車業は衰退していく一方です。<br>このことはつまり、近所の自転車を修理してくれる店がなくなることを意味するので利用者にとっても問題です。貴重な技術者が活躍することができる機会の確保が求められます。<br>
</p><p>
以上のように自転車が抱える問題は深刻で、かつ解決が困難なものが多いです。


</p><hr size="2" color="black" noshade=""><p>



「駐輪スペースがなく、放置自転車が多い。」「自転車の窃盗が多発している。」「販売店の衰退により修理やメンテナンスが困難。」の問題を効果的に解決する取組が<font size="7"><b>「レンタサイクル(コミュニティサイクル)」</b>です。</font>

</p><p>

<font size"5"=""> レンタサイクル(コミュニティサイクル)</font>
 
</p><p>
レンタサイクルの形態のひとつで、街の一定範囲内で、至るところに設置してある自転車を好きな場所で借りたり、返却することができるシステム。パリ、オスロ、バルセロナ、コペンハーゲンなどでは、中心市街地での公共交通の一環として、自転車シェアリングが根付いている。
 
自動車から、排気ガスなどの温室効果ガスを出さない自転車シェアリングに転換して普及すれば、地球温暖化防止になるほか、渋滞対策にもなる。料金体系は都市によって異なるが時間に応じて課金するシステムで、短時間なら無料という都市もある。クレジットカードや電子マネーなどを利用し、無人で精算することができるので、貸し借りに時間がかからず手軽に利用することができる。運営側の収入源は利用料のほか、民間の広告掲示料がある。<br>
