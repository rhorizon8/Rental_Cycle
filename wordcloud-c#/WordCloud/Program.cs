<a name="third"></a></p><h3><a name="third">第三章　　政策としての自転車の位置づけ</a></h3>
<p>
　日本では、東京オリンピック後にモータリゼーションが起こり、自動車の普及が急速に進んだ。それまでは、クルマ、自転車がお互いに譲り合って走行していたが、自動車の普及によって、事故が多発するようになった。<br>
　日本は国土が狭いので、クルマと自転車、どちらも快適に走れるほどのスペースの確保は困難である。<br>
　そこで、日本政府が取った対応は、自転車が歩道を走れるようにルールを変えるという政策であった。これは自転車にとっては嬉しくない政策であった。<b>つまりクルマと自転車の車道での両立は困難なので、自動車に偏った政策を出すことで自転車を歩道においやったのである。</b><br>
　それは、平均時速約１５キロの自転車と平均時速５キロの歩行者が、約１メートルの幅しかない道で、お互いが快適に移動することは現実的に不可能だからである。<br>
　これによって、自転車は不便さや不快感を感じるようになり、自転車衰退の一因となっている。<br>
</p><p>
