<a name="second"></a></p><h3><a name="second">第二章　日本特有の「シティサイクル」文化</a></h3>
<p>
<b>日本しかない自転車文化</b><br>
</p><p>
　海外での自転車は、日本における自転車とは大きく違った文化である。海外の自転車は「サイクリング」をするためのモノである。だから、一台何万円もする様な高級なモノを持ち、自転車ウェアを着て走る本格的な趣味やスポーツとしての自転車しかない。つまり、欧米では自転車は「スポーツサイクル」なのだ。<br>
</p><p>　
　一方、以前は、交通手段の一つという特殊な側面も併せ持つ日本において、最初の自転車ブームが来たのは、戦後復興の１９５０年代である。当時の日本は貧しく、移動のためのエネルギーもろくになかった。貧しく、国土の狭い日本において、自転車とは、「交通手段」であった。現在でも、「ママチャリ」という世界でも珍しい、普段の日常生活において通勤・通学など、様々な場面で気軽に使用する、「シティサイクル」という日本独自の文化がある。<br>
　つまり、<b>海外では「運動」としての自転車のイメージが強いが、日本では「手段」としての自転車のイメージが強い。</b>
