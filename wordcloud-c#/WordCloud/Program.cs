</p><h3>政策提言　</h3>
<p>
レンタサイクル導入には以下の問題を乗り越える必要があります。
</p><p>コミュニティサイクルは交通やまちづくりの課題を解決す
るための一つの手段
</p><p>まちづくり施策の一環として導入することが重要
<br>①まちづくりや交通の課題を踏まえ、導入目的を整理する。
（観光か？商店街振興か？公共交通端末か？）
<br>②まちづくり施策、公共交通との連携等を行う。
<br>③自転車走行空間、放置自転車対策、ルールの徹底などの
自転車施策とセットで行う。
<br>④その上で、適切なシステムを検討していく。
</p><p>
日本は現在、「環境」「経済」「交通」「健康」など様々な分野で問題に直面しています。これらを効果的に解決してくれるのが「自転車」であると私は考えます。しかし過去の日本において自転車は「見放された移動手段」であり、今の日本で自転車を利用することは困難な状況にあります。日本の交通分担量の大半を占める「クルマ」から「自転車」にシフトするためには多くの問題があります。その中でも重要なのが、人々が自転車を利用することが不便でデメリットが大きいと思っていることだと思います。自転車の利用を促進することで様々な問題を解決するためには、人々が自転車を利用しやすくすることが求められます。そのためのシステムが「レンタサイクル」であると私は考えます。今までの観光客のための「レンタサイクル」ではなく、観光客やその地域の住民も含めてみんなで共有できる「レンタサイクル」を整備することが今後の日本には求められると思います。
</p><p>

自転車以外にも文化の発展によって見放されてきたものが多くあると思います。近代化が頭打ち状態になり、様々な問題に世界が直面しています。これらを解決するヒントが過去の産物にもあると思います。



