</p><h4>文部科学省</h4>
<p>
これまでの文部科学省の自転車に対する考えはネガティブなものでした。その原因は子供が自転車にのる際、ルールを守らずに事故に遭うケースがとても多かったからです。いくら学校で交通教室を行ってもあまり効果はなく、文部科学省の出す自転車に関する政策
は問題をいかに解決するかというものが大半でした。

</p><h4>子供の発達と自転車の関係</h4>

<br>しかし、最近では子供の学習能力発達と自転車の関係が重要であるという見方が出てきました。Coventry大学のスポーツ科学部のMichael Duncanは、子供が自転車に乗ると読み書きの能力が向上するという研究報告をしています。現在「ゆとり」教育の悪い効果により世界における日本の学力レベルの低下が大きな問題になっています。学問を習得するうえでとても重要な「読み書き」の能力が、小さいころから自転車に乗ることで大きく成長するということで子供の自転車利用が求められています。

<h4>厚生労働省</h4>
<p>
放置自転車、マナーの悪い自転車利用者とのトラブルによって一般市民がストレスに感じ、駅前の公衆衛生が悪化することによって治安が悪くなったり、事故に遭い重大な被害が出たりなど多くの問題要素になっています。これらの問題は現在でも解決の具体的な取り組みがなされておらず、解決が望まれている。

</p><p>一方で自転車による健康へのメリットも近年注目されている。
厚生労働省「健康づくりのための運動指針」では生活習慣病の予防には効果的な運動が気軽に長期的に続けることが可能になる。また「メタボ」や「うつ病」など様々な効果をもたらしてくれるので日本国民の健康改善に大きな効果をもたらしてくれる。


</p><h4>環境省</h4>


全世界規模で環境問題は大きな問題となっている。最近ではエコカーブームが来ていて、政府はエコカー補助金を出してまでも環境に良い移動手段を促進しています。しかしいくら燃費の良いクルマを開発してもエネルギー使用量をゼロにするのは困難です。
環境省は「クルマ」から「自転車」への移動手段の転換政策を進めています。自転車はエネルギーコストもかからず、生産の段階でもクルマと比べてはるかに環境にやさしいので
地球温暖化問題や、資源の高騰といった社会的制約要因を考えると、低炭素社会や自然共生社会、循環型
社会を構築する上で、自転車利用の促進は重要です。

<h4>法務省</h4>
<p>今までは、自転車に関する法律はあったものの
実際にルールを理解している人が少なく、また中には法律は知っているのにも関わらず無視している人も多いなど法律として機能していませんでした。自転車も立派な「車両」であり事故が起きれば人を死なせてしまう危険も大いにあります。なのに法務省は長年自転車に関する法律の見直しをしてきませんでした。
</p><p>しかし２０１３年１２月１日に道路交通法が改正され、正式に自転車の逆走が禁止になりました。長年自転車に関する法律があいまいであることが問題になっていたのにも関わらず長い間放置されていました。その自転車に関する法律が改正されたということは政府がようやく自転車の有効性を認めた証拠であると思います。
他にもブレーキのついていない自転車（ピストバイク）が行動を走ることを禁止したなど今後も自転車に関する法律が見直されていくでしょう。

