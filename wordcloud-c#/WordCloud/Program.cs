<p>
</p><h3>第四章　政策として自転車を推す理由</h3>
<p>
　政府が自転車を推すのには、もちろん理由がある。<br>
例えば、<br>
</p><ul>
<li>環境<br>
　世界では地球温暖化問題が深刻になり、先進国である日本も環境保護を重視した政策を打ち出す理由がある。<br>
　交通におけるエコ実現のために最も問題視されているのがクルマである。最近エコカーという言葉を良く耳にするように「クルマ」がもたらす環境へのダメージは深刻である。<br>
　クルマの性能を向上させて環境に配慮するよりも、クルマの利用そのものを減らす取り組みとして「自転車」の利用が求められている。<br>
<p>
</p></li><li>健康<br>
　自転車は健康面での期待も大きい。近年の日本人の健康面での問題は、「メタボ」と「自殺者の多さ」である。メタボ対策に必要なのは、何といっても運動が不可欠である。運動には水泳、スポーツ、ジム、など様々な運動をする機会がありますが時間的、金銭的問題がありなかなか長続きしない。<br>
　自転車なら坂道を登る時やギアを重くした時に無酸素運動になる。自転車は日常で良く使う乗り物であるし、景色を楽しみながら無理なく楽しく、しかも、財布にも環境にも優しく運動をすることが出来る。<br>
　また、肉体的運動に加え、ストレス解消にも大きな効果がある。自転車には順番待ちやすし詰め状態もないので、通勤・通学時のストレスがかからない上に、景色や運動によって心が晴れる。これらによって、日本が抱える健康問題である「メタボ」と「自殺」にも効果的である。<br>
</li></ul>
<p>
</p><hr size="2" color="black" noshade="">
<p>
　以下では詳細に、日本の政策としての自転車に焦点を当てていく。<b>各省庁それぞれの立場からの自転車の有用性が注目されてきている。</b>
</p><p>
</p><h4>国土交通省から見た自転車</h4>
<ul>
<li>国土交通省は移動手段を主に担当している。<br>
　日本は昭和４０年代にモータリゼーションが起きて自動車が急速に普及した。以前は自転車と自動車は比較的に対等な立場にあり、お互いに譲り合って走行していた。しかしこのモータリゼーションにより自動車と自転車の事故が多発する様になると自動車優位が確立した。日本国政府も、日本は国土が狭いために自転車と自動車の両社のための走行スペースを確保することが困難なので、自転車を歩道走行を認める政策をとることで自転車を車道から追い出すようになった。
しかし、「平均時速３キロの歩行者」と「平均時速１５キロの自転車」が狭い空間で共存することは事実上不可能である。

</li><li>コンパクトシティへの転換の必要<br>
　現在、日本の都市の多くは都心部に高層建物と老朽した木造住宅が密集した地区が混在しており、環境や防災上の問題を抱えている一方で、利用されていない土地があちこちに散在するなど、無駄の多い姿になっています。また、市街地の無秩序な拡大は、都市を取り巻く農地や緑地の消失を招いています。
　街なか居住研究会では、高層化をしなくても、都心部を有効に活用することによって都市全体をコンパクトにでき、これらの問題を解決できると考えています。
　都市がコンパクトになれば、近郊の緑地や農地が保全できます。また、都心居住を進めることにより、職場と自宅が近くなり（職住近接）、通勤による渋滞を緩和することができるとともに、高齢者などの自家用車を利用しにくい人々が、歩いて商店街や公共公益施設を利用することができるようになります。
　さらに、都市の中心部にさまざまな機能を集めることによって、相乗的な経済交流活動が活発になり、中心市街地の活性化も期待できます。このことは、空洞化が深刻な東北の地方都市の都市部において、より重要な視点であるといえます。
つまり、今後は自転車でいける範囲内に、「病院」「学校」「スーパーや銀行」などができるようになります。
</li><li>コンパクトシティのための自転車の有用性<br>
今後、日本各地でコンパクトシティ化がすすめられていくと考えられます。この時に問題になるのが移動手段です。現在でメインに利用されている自動車は都市部においては道幅が狭かったり、駐車スペースの確保が困難だったりと様々な問題を引き起こします。
ここでレンタサイクルが効果を発揮してきます。
コンパクトシティのコンパクトな空間で生活全てを賄える街づくりの距離ならば、自転車の利用で十分可能であります。車がないと生活できない従来の田舎の生活においても、自転車は有効な移動手段になることができます。
</li></ul>
