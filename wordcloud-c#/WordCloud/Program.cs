</p><h4>コミュニティサイクル社会実験の利用状況のまとめ</h4>
<p>【需要】
<br>○都市中心部における利用ニーズは存在。鉄道の来街者による利用は多
い。
</p><p>【利用目的】
<br>○路上等において利便性を上げれば、日常の通勤・業務利用が発生。
<br>観光地施設を中心とすれば、観光利用。
</p><p>【考察】
<br>○導入台数が少なくても（数百台程度でも）、導入目的（商業、観光等）
に応じた適切なポート配置やシステムであれば、利用される可能性は
高い。
<br>→ 小さい規模で始め、段階的に拡大していける可能性も高い。
<br>○一方でターミナル駅などで朝の貸し出しの不足、夕方の返却の集中が
発生して偏りが非常に大きくなる。
<br>→ 運営上の工夫、配慮が重要。

<br>○通勤以外の日中の利用（商業、業務等）を増やして平準化を図る
ことが、平準化、採算性向上のために重要
<br>→ 法人の会員登録や観光利用などを増やして、日中の利用を増加
させることが重要。
</p><hr size="2" color="black" noshade=""><p>

</p><p>導入効果に対する考え方
<br>○コミュニティサイクルの利用は、徒歩や公共交通からの転換が中心で、
自動車からの転換は多くはない。
<br>○潜在的な意向として、自動車利用機会の減少の意向も見られる。
<br>→郊外からの公共交通利用との連携や、パークアンドライド（観光系）な
どとの連携を行い、交通施策の一環として取り組むことが必要。
<br>○自己所有の自転車からの転換も少なく、放置自転車の削減に資する効果
も大きくない。
<br>→放置自転車対策とセットで進めることが必要。
（買い物客や業務目的などの自転車駐車場の整備が必要）
<br>→長期的には、市街地に置きっぱなしの自転車は、転換の可能性有り。
<br>○コミュニティサイクルにより新たに行動が発現した利用者が確認され、
また、利用圏域の拡大や新たな交通手段としての活用という面で利用車
のから評価が高い。
<br>○地域における新たな移動・回遊性を高める可能性がある。
<br>→商店街や地元企業との連携などを行うことで地域活性化に寄与する可能
性が高く、まちづくりの一環として取り組むことが必要。

</p><hr size="2" color="black" noshade="">
