<a name="first"></a></p><h3><a name="first">第一章　自転車ブームが世界に来ている！</a></h3>
<p>
　ここ最近、自転車ブームが来ている。本屋には自転車関連の雑誌が並び、町に出てもスポーツ車（ロードレーサー）を見ない日はない。<br>
　自転車がブームになっている理由は、「ガソリン価格の高騰」、「メタボ予防」、「環境にも財布にも優しい」などがある。また、自転車は整備が簡単であり「手軽で便利な移動手段」として多くのメリットを持つ。これら様々な面でのメリットによって通勤・通学に用いる人はもちろん観光手段として、自転車への期待が高まっている。<br>
　自転車は、平成になって、急速に古臭いもの「昭和の象徴」のようなものとして扱われてきた。この先も自転車は衰退していく」一方かに思われたが、最近では自転車を見る機会が多くなってきた。<br>
　その再びの自転車ブームのきっかけになったのが、「電動自転車」や「スポーツバイク」の台頭である。今までの一般的なママチャリでは、「自転車での移動距離の限界はどの程度か？」というアンケートで、０～１キロが１１．９％、１～３キロが３１．７％、３～５キロが２３．９％、と５キロ以内で実に６７．５％の人が限界だと感じていた。しかし、この二つの自転車の発明によって、エネルギーは半分で、距離は今までの倍走れるようになり、自転車の可能性は大きく向上した。<br>
　しかし、これらだけでなく、様々な要因が自転車にとっての追い風になっている。<br>
