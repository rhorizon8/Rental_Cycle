<br>パリにおける自転車問題は「駐輪所が少ない」「盗難や破損」など日本と似た問題を抱えていた。これらの問題も同時に解決してくれるのがレンタサイクルの仕組みである。当初は750のポイントに１万台を設置、現在では1451ポイント、2万台に増えている。パリでの市内バス・メトロ一年間の定期が約９万円で、ヴェリブ一年間の定期が約５０００円となっている。<br>年中無休、２４時間営業なのも人気の理由だ。
